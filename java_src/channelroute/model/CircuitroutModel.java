package channelroute.model;

public class CircuitroutModel {

	private String id;
	private String equipcode;
	private String equipname;
	private String circuitcode;
	private String a_portcode;
	private String z_portcode;
	private String a_slot;
	private String z_slot;
	private String ccid;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getEquipcode() {
		return equipcode;
	}
	public void setEquipcode(String equipcode) {
		this.equipcode = equipcode;
	}
	public String getEquipname() {
		return equipname;
	}
	public void setEquipname(String equipname) {
		this.equipname = equipname;
	}
	public String getCircuitcode() {
		return circuitcode;
	}
	public void setCircuitcode(String circuitcode) {
		this.circuitcode = circuitcode;
	}
	public String getA_portcode() {
		return a_portcode;
	}
	public void setA_portcode(String a_portcode) {
		this.a_portcode = a_portcode;
	}
	public String getZ_portcode() {
		return z_portcode;
	}
	public void setZ_portcode(String z_portcode) {
		this.z_portcode = z_portcode;
	}
	public String getA_slot() {
		return a_slot;
	}
	public void setA_slot(String a_slot) {
		this.a_slot = a_slot;
	}
	public String getZ_slot() {
		return z_slot;
	}
	public void setZ_slot(String z_slot) {
		this.z_slot = z_slot;
	}
	public String getCcid() {
		return ccid;
	}
	public void setCcid(String ccid) {
		this.ccid = ccid;
	}
}
