package channelroute.model;

public class StringUtil {

	/**
	 * 
	 * @param strToTest
	 * @return
	 */
	public static boolean isEmpty(String strToTest) {
		if(strToTest == null || "".equals(strToTest)) {
			return true;
		}else {
			return false;
		}
	}
	
	/**
	 * 
	 * @param strToTest
	 * @return
	 */
	public static boolean isNotEmpty(String strToTest) {
		if(strToTest == null || "".equals(strToTest)) {
			return false;
		}else {
			return true;
		}
	}

}
