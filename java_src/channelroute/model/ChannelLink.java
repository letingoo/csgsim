package channelroute.model;

public class ChannelLink {
	/**
	 * A端口
	 */
	private ChannelPort portA;
	/**
	 * Z端口
	 */
	private ChannelPort portZ;
	/**
	 * 是否复用段
	 */
	private Boolean isTopo;
	private String topoid;
	/**
	 * @return the portA
	 */
	public ChannelPort getPortA() {
		return portA;
	}
	/**
	 * @param portA the portA to set
	 */
	public void setPortA(ChannelPort portA) {
		this.portA = portA;
	}
	/**
	 * @return the portZ
	 */
	public ChannelPort getPortZ() {
		return portZ;
	}
	/**
	 * @param portZ the portZ to set
	 */
	public void setPortZ(ChannelPort portZ) {
		this.portZ = portZ;
	}
	/**
	 * @return the isTopo
	 */
	public Boolean getIsTopo() {
		return isTopo;
	}
	/**
	 * @param isTopo the isTopo to set
	 */
	public void setIsTopo(Boolean isTopo) {
		this.isTopo = isTopo;
	}
	/**
	 * @return the topoid
	 */
	public String getTopoid() {
		return topoid;
	}
	/**
	 * @param topoid the topoid to set
	 */
	public void setTopoid(String topoid) {
		this.topoid = topoid;
	}
	
	

}
