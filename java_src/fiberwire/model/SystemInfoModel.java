package fiberwire.model;

public class SystemInfoModel 
{
	private String systemcode;
	private String systemname;
	private String x_capacity;
	private String projectname;
	private String tranmodel;
	private String vendor;
	private String remark;
	private String x;
	private String y;
	
	public SystemInfoModel()
	{
		this.systemcode = "";
		this.systemname = "";
		this.vendor="";
		this.remark="";
		this.x="0";
		this.y="0";
	}

	public String getSystemcode() {
		return systemcode;
	}

	public void setSystemcode(String systemcode) {
		this.systemcode = systemcode;
	}

	public String getSystemname() {
		return systemname;
	}

	public void setSystemname(String systemname) {
		this.systemname = systemname;
	}

	public String getVendor() {
		return vendor;
	}

	public void setVendor(String vendor) {
		this.vendor = vendor;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}

	public String getX_capacity() {
		return x_capacity;
	}

	public void setX_capacity(String x_capacity) {
		this.x_capacity = x_capacity;
	}

	public String getProjectname() {
		return projectname;
	}

	public void setProjectname(String projectname) {
		this.projectname = projectname;
	}

	public String getTranmodel() {
		return tranmodel;
	}

	public void setTranmodel(String tranmodel) {
		this.tranmodel = tranmodel;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	

}
