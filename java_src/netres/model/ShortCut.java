package netres.model;

public class ShortCut {
private String user_id;
private String shortcut_name;
private String shortcut_ID;
private String user_state;
public String getUser_id() {
	return user_id;
}
public void setUser_id(String user_id) {
	this.user_id = user_id;
}
public String getShortcut_name() {
	return shortcut_name;
}
public void setShortcut_name(String shortcut_name) {
	this.shortcut_name = shortcut_name;
}
public String getShortcut_ID() {
	return shortcut_ID;
}
public void setShortcut_ID(String shortcut_ID) {
	this.shortcut_ID = shortcut_ID;
}
public String getUser_state() {
	return user_state;
}
public void setUser_state(String user_state) {
	this.user_state = user_state;
}

}
