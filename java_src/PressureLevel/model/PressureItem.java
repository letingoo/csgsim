package PressureLevel.model;

public class PressureItem {

	
	private String level;
	
	private int weight;
	
	
	public String getLevel() {
		return level;
	}
	
	public int getWeight() {
		return weight;
	}
	
	public void setLevel(String level) {
		this.level = level;
	}
	
	public void setWeight(int weight) {
		this.weight = weight;
	}
	
}
