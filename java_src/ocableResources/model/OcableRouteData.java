package ocableResources.model;

import java.util.List;


public class OcableRouteData {
	/** 图形XML */
	String xml;

	/** 局站或T接列表 */
	List<OcableStation> stationList;

	/** A向路由 */
	List listRouteA;

	/** B向路由 */
	List listRouteB;

	/** A向路由列表XML */
	private String routeXmlA;

	/** A向路由列表XML */
	private String routeXmlB;

	private OcableRouteContent content;
	
	
	public OcableRouteContent getContent() {
		return content;
	}

	public void setContent(OcableRouteContent content) {
		this.content = content;
	}

	public String getXml() {
		return xml;
	}

	public void setXml(String xml) {
		this.xml = xml;
	}

	public List<OcableStation> getStationList() {
		return stationList;
	}

	public void setStationList(List<OcableStation> stationList) {
		this.stationList = stationList;
	}

	public List getListRouteA() {
		return listRouteA;
	}

	public void setListRouteA(List listRouteA) {
		this.listRouteA = listRouteA;
	}

	public List getListRouteB() {
		return listRouteB;
	}

	public void setListRouteB(List listRouteB) {
		this.listRouteB = listRouteB;
	}

	public String getRouteXmlA() {
		return routeXmlA;
	}

	public void setRouteXmlA(String routeXmlA) {
		this.routeXmlA = routeXmlA;
	}

	public String getRouteXmlB() {
		return routeXmlB;
	}

	public void setRouteXmlB(String routeXmlB) {
		this.routeXmlB = routeXmlB;
	}


}
