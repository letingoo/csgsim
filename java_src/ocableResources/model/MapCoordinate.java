package ocableResources.model;

public class MapCoordinate {
	private String coordinateid;
	private String modelname;
	private String province;
	private String stationcode;
	private String istnode;
	private String node_x;
	private String node_y;
	private String label_x;
	private String label_y;
	public String getModelname() {
		return modelname;
	}
	public void setModelname(String modelname) {
		this.modelname = modelname;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getStationcode() {
		return stationcode;
	}
	public void setStationcode(String stationcode) {
		this.stationcode = stationcode;
	}
	public String getIstnode() {
		return istnode;
	}
	public void setIstnode(String istnode) {
		this.istnode = istnode;
	}
	public String getNode_x() {
		return node_x;
	}
	public void setNode_x(String nodeX) {
		node_x = nodeX;
	}
	public String getNode_y() {
		return node_y;
	}
	public void setNode_y(String nodeY) {
		node_y = nodeY;
	}
	public String getLabel_x() {
		return label_x;
	}
	public void setLabel_x(String labelX) {
		label_x = labelX;
	}
	public String getLabel_y() {
		return label_y;
	}
	public void setLabel_y(String labelY) {
		label_y = labelY;
	}
	public String getCoordinateid() {
		return coordinateid;
	}
	public void setCoordinateid(String coordinateid) {
		this.coordinateid = coordinateid;
	}
}
